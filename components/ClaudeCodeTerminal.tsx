type Status = {
  enabled?: boolean;
  configuredBin?: string;
  executablePath?: string;
  executableExists?: boolean;
  runningSessions?: number;
  isPackaged?: boolean;
};

type SelfModStatus = {
  safeMode?: boolean;
  manualSafeMode?: boolean;
  failCount?: number;
  state?: string;
  snapshot?: {
    exists?: boolean;
    meta?: {
      reason?: string;
      createdAt?: number;
    } | null;
  };
};

type ClaudeSessionMeta = {
  id: string;
  cwd: string;
  createdAt: number;
  attachments: string[];
};

type FileNode = {
  name: string;
  path: string;
  isDirectory: boolean;
  size: number;
  mtime: Date;
  children?: FileNode[];
};

type PreviewState = {
  path: string;
  name: string;
  type: string;
  content: string;
  error?: string;
};

type TokenStats = {
  input: number;
  output: number;
  cache: number;
  total: number;
  costUsd: number;
  updatedAt: number;
};

